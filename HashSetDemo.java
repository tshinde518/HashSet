// Write a Java program to append the specified element to the end of a hash set.  

import java.util.*;
class HashSetDemo
{
  public static void main(String[] args)
  {
   Set<String>fruits=new HashSet<>();
    fruits.add("Mango");
    fruits.add("Apple");
    fruits.add("Banana");
    System.out.println(fruits);
  }
}